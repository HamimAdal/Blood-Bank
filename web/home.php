        <h1 style="color:#000000">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Welcome to MIST Blood Bank</h1>
        <p style="color:#FF0000">Blood donation is one of the noblest and greatest donations a man can make. Blood is our life-sap. It has no substitute. Several lives are lost for want of blood. Doctors need blood for transfusing it in the body of persons after operation and similar situations when a lot of blood flows out of the person’s body.</p>
        <p style="color:#FF0000">Now-a-days the Blood Banks stores this blood in sterilized bottles which are to be supplied in the time of need. But the increasing demand for blood at hospitals and other health centers cannot be met up by the limited number of professional blood sellers. We must all come forward to donate blood.</p>
        <p style="color:#FF0000">People fewer than eighteen and above sixty cannot donate blood. A donor must be at least 47.5 kg by weight. A past history of jaundice and certain other infectious diseases contraindicate blood donation..</p>
        <p style="color:#FF0000">Earlier people had a fear that blood-donation would weaken their body-strength and hamper their natural immunity. Now-a-days people have a scientific bent of mind. Once they are made aware of the fact that the amount of blood taken out from their body is minimal and easily replenished, they can easily let their fears go off.</p>
		<p style="color:#FF0000">People should understand that their little contribution can prove to be of great help to others. He is also personally benefited. He receives a permit which enables him to fetch a bottle of blood within a year, free of cost. Who knows whether he himself or his near or dear ones, would be saved by that bottle of blood, or not?</p>
		<p style="color:#FF0000">We should all come forward and donate blood if we are able-bodied because this one simple action would speak for itself about our gentleness, fellow-feelings, kindness and love for humanity. God has created life in this world. By donating blood to the life in need of blood, we save God’s children. Hence by serving man, we serve God.</p>
		<p style="color:#FF0000">MIST Blood Bank is a way where you can find donors who can donate blood and save your life.</p>
